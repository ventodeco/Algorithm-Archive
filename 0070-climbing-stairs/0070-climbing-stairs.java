class Solution {
    public int climbStairs(int n) {
        

        // 0 = 0
        // 1 = 1
        // 2 = 2 -> 1-1 | 2
        // 3 = ? -> 1-1-1 | 1-2 | 2-1
        // 4 = ? -> 1-1-1-1 | 1-1-2 | 1-2-1 | 2-1-1 | 2-2
        // 5 = ? -> 1-1-1-1-1 | 1-1-1-2 | 1-1-2-1 | 1-2-1-1 | 2-1-1-1 | 2-2-1 | 2-1-2 | 1-2-2

        // 1-2-3-5-8

        if (n <= 2) {
            return n;
        }

        int[] dp = new int[n];

        dp[0] = 1;
        dp[1] = 2;

        for (int i = 2; i < n; i++) {
            dp[i] = dp[i - 1] + dp[i - 2];
        }

        return dp[n - 1];
    }
}